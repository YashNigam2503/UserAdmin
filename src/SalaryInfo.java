
public class SalaryInfo {
	private String username ;
	private int Basic_Salary;
	private int GeneralAllowance;
	private String EmpID;
	public String getUserName() {
		return username;
	}
	public void setUserName(String userName) {
		username = userName;
	}
	public int getGeneralAllowance() {
		return GeneralAllowance;
	}
	public void setGeneralAllowance(int generalAllowance) {
		GeneralAllowance = generalAllowance;
	}
	public int getBasic_Salary() {
		return Basic_Salary;
	}
	public void setBasic_Salary(int basic_Salary) {
		Basic_Salary = basic_Salary;
	}
	public String getEmpID() {
		return EmpID;
	}
	public void setEmpID(String eID) {
		EmpID = eID;
	}
}
